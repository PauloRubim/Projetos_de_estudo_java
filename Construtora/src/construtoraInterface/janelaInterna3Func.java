    private void jTable3InternaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable3InternaMouseClicked
       
            Point p = evt.getPoint();  
            int row = jTable3Interna.rowAtPoint(p);  
            int col = jTable3Interna.columnAtPoint(p);  
            //System.out.printf("row: %d    col: %d\n", row, col);
            int ID = Integer.parseInt(jTable3Interna.getValueAt(row, 0).toString());
            String CPF = jTable3Interna.getValueAt(row, 1).toString();
            String Nome = jTable3Interna.getValueAt(row, 2).toString();
            String Sexo = jTable3Interna.getValueAt(row, 3).toString();
            String Nasc = jTable3Interna.getValueAt(row, 4).toString();
            double Salario = Double.parseDouble(jTable3Interna.getValueAt(row, 5).toString());
            boolean Disponibilidade = Boolean.valueOf(jTable3Interna.getValueAt(row, 6).toString());                                                 
        if (evt.getClickCount() == 2){             
            super.setDadosFunc(ID, CPF, Nome, Sexo, Nasc, Salario, Disponibilidade);
            this.setVisible(false);
        }                         
    }//GEN-LAST:event_jTable3InternaMouseClicked


