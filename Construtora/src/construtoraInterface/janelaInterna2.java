    private void jExcluirMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jExcluirMatActionPerformed
        if(jTable2.getSelectedRow() != -1){
            
            int Id = Integer.valueOf(jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString());            
            System.out.println(Id);
            
            
            EstabelecerConexao con = new EstabelecerConexao();
            MaterialDAO materialdao = new MaterialDAO(con);
            materialdao.DeletarMaterial(Id);
            
            DefaultTableModel tableMat = (DefaultTableModel) jTable2.getModel();//Tabela
            tableMat.removeRow(jTable2.getSelectedRow());                        
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir!");
        }   
    }//GEN-LAST:event_jExcluirMatActionPerformed

    private void jCadastrarMatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCadastrarMatActionPerformed
        int i = 0;
        //Adiciona no JTableMat   
        if(this.isNullOrEmpty(jtxtNome.getText()) || this.isNullOrEmpty(jtxtFornecedor.getText()) || this.isNullOrEmpty(jtxtQtd.getText()) || this.isNullOrEmpty(jtxtPreco.getText())){
             JOptionPane.showMessageDialog(null, "Por favor preencha todos os campos!");
        }else{
            EstabelecerConexao con = new EstabelecerConexao();
            MaterialDAO MatDao = new MaterialDAO(con);
            Material m = new Material();
            
            m.setNome(jtxtNome.getText());
            m.setFornecedor(jtxtFornecedor.getText());
            m.setEstoque(Integer.parseInt(jtxtQtd.getText()));
            m.setPreco(Double.parseDouble(jtxtPreco.getText()));
            //Adiciona no Banco de Dados
            MatDao.AdicionaMaterial(m);
            
            //Adiciona no JTable
            DefaultTableModel tableMat = (DefaultTableModel) jTable2.getModel();
            Object[] objMateriais = {m.getId(), jtxtNome.getText(), jtxtFornecedor.getText(), Integer.parseInt(jtxtQtd.getText()), Double.parseDouble(jtxtPreco.getText())};
            tableMat.addRow(objMateriais);
            this.apaga();
            this.recupera();
        }                 
    }//GEN-LAST:event_jCadastrarMatActionPerformed

