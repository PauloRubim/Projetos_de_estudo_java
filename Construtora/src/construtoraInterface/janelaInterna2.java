    private void apaga(){
        DefaultTableModel tableMat = (DefaultTableModel) jTable2.getModel();
        while(tableMat.getRowCount() != 0){
                tableMat.removeRow(0);                
            }
    }
    
    private void recupera(){
        EstabelecerConexao con = new EstabelecerConexao();
        MaterialDAO MatDao = new MaterialDAO(con);
        
        ArrayList<Material> arraymat = MatDao.RecuperaTudo();
        System.out.println(arraymat);
        
        //Recuperar os Funcionarios do Banco de Dados e adiciona na JTableMaterial       
        DefaultTableModel tabFunc = (DefaultTableModel) jTable2.getModel();
        for(Material m: arraymat){            
            Object[] objMateriais = {m.getId(), m.getNome(), m.getFornecedor(), m.getEstoque(), m.getPreco()};
            tabFunc.addRow(objMateriais);
        }
    }

